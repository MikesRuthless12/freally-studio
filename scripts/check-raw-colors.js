// check-raw-colors.js — the raw-color gate (TASK-C04)
//
// Scans src/**/*.{js,jsx,css} for raw hex / rgb() / hsl() literals. Every
// color must come from a token in src/ui/tokens.css.
//
//   node scripts/check-raw-colors.js                 report (always exit 0)
//   node scripts/check-raw-colors.js --block         exit 1 on violations
//   node scripts/check-raw-colors.js --write-inventory docs/ui-migration-list.md
//
// Allowlist: src/ui/tokens.css and src/ui/skins/ (the ONLY places colors are
// defined), plus any single line carrying a `raw-color-ok` comment — reserved
// for canvas code that genuinely cannot use var() and reads tokens via
// getComputedStyle instead.

import { readFileSync, writeFileSync, readdirSync, statSync } from 'node:fs';
import { join, relative, sep } from 'node:path';

const COLOR_RE = /#[0-9a-fA-F]{3,8}\b|rgba?\s*\(|hsla?\s*\(/g;

const ALLOWED_PATHS = [
    ['src', 'ui', 'tokens.css'].join(sep),
    ['src', 'ui', 'skins'].join(sep) + sep,
];

export function isAllowedFile(relPath) {
    return ALLOWED_PATHS.some(p => relPath === p || relPath.startsWith(p));
}

/** Scan one file's source; returns [{ line, count, sample }]. */
export function scanSource(text) {
    const violations = [];
    const lines = text.split('\n');
    for (let i = 0; i < lines.length; i++) {
        const line = lines[i];
        if (line.includes('raw-color-ok')) continue; // explicit canvas escape hatch
        const matches = line.match(COLOR_RE);
        if (matches && matches.length) {
            violations.push({ line: i + 1, count: matches.length, sample: line.trim().slice(0, 90) });
        }
    }
    return violations;
}

function walk(dir, out = []) {
    for (const entry of readdirSync(dir)) {
        const full = join(dir, entry);
        const st = statSync(full);
        if (st.isDirectory()) walk(full, out);
        else if (/\.(js|jsx|css)$/.test(entry)) out.push(full);
    }
    return out;
}

export function runScan(rootDir) {
    const srcDir = join(rootDir, 'src');
    const results = [];
    for (const file of walk(srcDir)) {
        const rel = relative(rootDir, file);
        if (isAllowedFile(rel)) continue;
        const violations = scanSource(readFileSync(file, 'utf8'));
        if (violations.length) {
            results.push({
                file: rel.split(sep).join('/'),
                lines: violations.length,
                refs: violations.reduce((n, v) => n + v.count, 0),
            });
        }
    }
    results.sort((a, b) => b.refs - a.refs);
    return results;
}

// ── CLI ──
const isMain = process.argv[1] && import.meta.url.endsWith(process.argv[1].split(sep).join('/').split('/').pop());
if (isMain) {
    const args = process.argv.slice(2);
    const block = args.includes('--block');
    const invIdx = args.indexOf('--write-inventory');
    const rootDir = process.cwd();

    const results = runScan(rootDir);
    const totalRefs = results.reduce((n, r) => n + r.refs, 0);

    console.log(`check-raw-colors: ${results.length} files, ${totalRefs} raw color references`);
    for (const r of results.slice(0, 20)) {
        console.log(`  ${String(r.refs).padStart(5)}  ${r.file}`);
    }
    if (results.length > 20) console.log(`  … and ${results.length - 20} more files`);

    if (invIdx >= 0) {
        const outPath = args[invIdx + 1] || 'docs/ui-migration-list.md';
        const md = [
            '# UI migration list — raw color inventory (TASK-C04)',
            '',
            'Generated by `node scripts/check-raw-colors.js --write-inventory`.',
            'Every entry must reach zero before the gate flips to `--block` in',
            '`npm run check` (TASK-C09). Colors are only allowed in',
            '`src/ui/tokens.css` and `src/ui/skins/` (plus `raw-color-ok` canvas lines).',
            '',
            `Total: **${results.length} files / ${totalRefs} raw color references**`,
            '',
            '| Raw refs | Lines | File |',
            '|---:|---:|---|',
            ...results.map(r => `| ${r.refs} | ${r.lines} | \`${r.file}\` |`),
            '',
        ].join('\n');
        writeFileSync(join(rootDir, outPath), md);
        console.log(`inventory written to ${outPath}`);
    }

    if (block && totalRefs > 0) {
        console.error('check-raw-colors: BLOCKING — raw colors found outside the allowlist.');
        process.exit(1);
    }
}
