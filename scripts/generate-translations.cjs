// Write all locale files
function writeLocale(langCode, translations, existingPath) {
  const existing = require(existingPath);
  const merged = {};

  // Go through all EN keys in order
  for (const key of enKeys) {
    if (existing[key] !== undefined) {
      merged[key] = existing[key];
    } else if (translations[key] !== undefined) {
      merged[key] = translations[key];
    } else {
      // Fallback: use English value (should not happen if translations are complete)
      merged[key] = en[key];
      console.warn(`WARNING: ${langCode} missing translation for: ${key}`);
    }
  }

  const outPath = existingPath;
  fs.writeFileSync(outPath, JSON.stringify(merged, null, 2) + '\n', 'utf8');

  const missingCount = enKeys.filter(k => !(k in existing) && !(k in translations)).length;
  console.log(`${langCode}: wrote ${Object.keys(merged).length} keys (${missingCount} fell back to English)`);
}

