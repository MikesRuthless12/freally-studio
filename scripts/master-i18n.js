// Due to the massive size, we'll split the remaining languages into a Part 2 script
// This script handles: af, ar, az, be, bg, bs
// Part 2-8 scripts will handle the remaining languages

const total = Object.keys(TRANSLATIONS).length;
let completed = 0;

for (const [lang, keys] of Object.entries(TRANSLATIONS)) {
  const filePath = path.join(LOCALES_DIR, `${lang}.json`);

  if (!fs.existsSync(filePath)) {
    console.log(`[${++completed}/${total}] ${lang}.json - SKIPPED (file not found)`);
    continue;
  }

  const content = fs.readFileSync(filePath, 'utf8');
  const json = JSON.parse(content);

  let added = 0;
  for (const [key, value] of Object.entries(keys)) {
    if (!json[key]) {
      json[key] = value;
      added++;
    }
  }

  const output = JSON.stringify(json, null, 2) + '\n';
  fs.writeFileSync(filePath, output, 'utf8');

  console.log(`[${++completed}/${total}] ${lang}.json - ${added} keys added ✓`);
}

console.log(`\nPart 1 complete: ${completed}/${total} languages processed.`);
