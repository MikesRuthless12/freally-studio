// Process each locale file
const localeFiles = fs.readdirSync(LOCALES_DIR).filter(f => f.endsWith('.json') && f !== 'en.json');
let updated = 0;

for (const file of localeFiles) {
  const lang = file.replace('.json', '');
  const filePath = path.join(LOCALES_DIR, file);
  const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));
  let changed = false;

  for (const [key, translations] of Object.entries(NEW_KEYS)) {
    if (!data[key]) {
      data[key] = translations[lang] || translations.en;
      changed = true;
    }
  }

  if (changed) {
    fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf8');
    updated++;
  }
}

console.log(`Updated ${updated} locale files with ${Object.keys(NEW_KEYS).length} tooltip keys.`);
