// Localized phrase bank reference — set by PhraseLoader before generation
let _localizedPhraseBank = null;

/**
 * Set the localized phrase bank for non-English generation.
 * Called by LyricEngine before generating.
 * @param {object|null} bank
 */
export function setLocalizedPhraseBank(bank) {
    _localizedPhraseBank = bank;
}

/**
 * Get phrases for a given genre, falling back to pop if unknown.
 * Checks for localized phrase bank data before English built-in.
 * @param {string} genre
 * @returns {object}
 */
export function getGenreBank(genre) {
    // Check for localized phrase bank
    if (_localizedPhraseBank?.genreBanks) {
        const key = genre.toLowerCase().replace(/[\s-]/g, '');
        const localGenre = _localizedPhraseBank.genreBanks[key] || _localizedPhraseBank.genreBanks['pop'];
        if (localGenre) return localGenre;
    }

    const key = genre.toLowerCase().replace(/[\s-]/g, '');
    return GENRE_BANKS[key] || GENRE_BANKS.pop;
}

/**
 * Get mood modifier for a given mood, or null.
 * Checks for localized phrase bank data before English built-in.
 * @param {string} mood
 * @returns {object|null}
 */
export function getMoodModifier(mood) {
    // Check for localized phrase bank
    if (_localizedPhraseBank?.moodModifiers) {
        const key = mood.toLowerCase().replace(/[\s-]/g, '');
        if (_localizedPhraseBank.moodModifiers[key]) return _localizedPhraseBank.moodModifiers[key];
    }

    const key = mood.toLowerCase().replace(/[\s-]/g, '');
    return MOOD_MODIFIERS[key] || null;
}

/**
 * Apply mood modifier swaps to a phrase.
 * @param {string} phrase
 * @param {string} mood
 * @returns {string}
 */
export function applyMoodToPhrase(phrase, mood) {
    const mod = getMoodModifier(mood);
    if (!mod || !mod.swaps) return phrase;
    let result = phrase;
    for (const [from, to] of Object.entries(mod.swaps)) {
        result = result.replace(new RegExp(`\\b${from}\\b`, 'gi'), to);
    }
    return result;
}

/**
 * Get a random item from an array using a seeded random.
 * @param {Array} arr
 * @param {function} rng - random function returning 0-1
 * @returns {*}
 */
export function pick(arr, rng) {
    if (!arr || arr.length === 0) return '';
    return arr[Math.floor((rng ? rng() : Math.random()) * arr.length)];
}

