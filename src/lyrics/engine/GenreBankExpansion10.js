export function mergeGenreBanks10(target, source) {
    for (const [genre, data] of Object.entries(source)) {
        if (!target[genre]) target[genre] = {};
        for (const [key, arr] of Object.entries(data)) {
            if (Array.isArray(arr)) {
                if (!target[genre][key]) target[genre][key] = [];
                target[genre][key].push(...arr);
            }
        }
    }
}
