/**
 * Merge hook template expansions into existing arrays.
 */
export function mergeHookTemplates(existingTemplates, expansion) {
    const existingSet = new Set(existingTemplates.map(t => t.toLowerCase()));
    for (const template of expansion) {
        if (!existingSet.has(template.toLowerCase())) {
            existingTemplates.push(template);
        }
    }
}

/**
 * Merge genre-specific hook templates.
 */
export function mergeGenreHookTemplates(existing, expansion) {
    for (const [genre, templates] of Object.entries(expansion)) {
        if (Array.isArray(existing[genre])) {
            const existingSet = new Set(existing[genre].map(t => t.toLowerCase()));
            for (const template of templates) {
                if (!existingSet.has(template.toLowerCase())) {
                    existing[genre].push(template);
                }
            }
        } else {
            existing[genre] = templates;
        }
    }
}

/**
 * Merge chorus templates.
 */
export function mergeChorusTemplates(existingArr, expansion) {
    const existingSet = new Set(existingArr.map(t => t.toLowerCase()));
    for (const template of expansion) {
        if (!existingSet.has(template.toLowerCase())) {
            existingArr.push(template);
        }
    }
}

/**
 * Merge blocklist words.
 */
export function mergeBlocklist(existingSet, expansion) {
    for (const word of expansion) {
        existingSet.add(word.toLowerCase());
    }
}
