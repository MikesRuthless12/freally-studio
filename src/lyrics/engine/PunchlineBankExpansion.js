/**
 * Merge expansion punchlines into existing PUNCHLINE_PHRASES.
 */
export function mergePunchlinePhrases(existing, expansion) {
    for (const [family, data] of Object.entries(expansion)) {
        if (!existing[family]) {
            existing[family] = data;
        } else {
            for (const [mood, phrases] of Object.entries(data)) {
                if (Array.isArray(phrases)) {
                    if (Array.isArray(existing[family][mood])) {
                        // Avoid duplicates
                        const existingSet = new Set(existing[family][mood].map(p => p.toLowerCase()));
                        for (const phrase of phrases) {
                            if (!existingSet.has(phrase.toLowerCase())) {
                                existing[family][mood].push(phrase);
                            }
                        }
                    } else {
                        existing[family][mood] = phrases;
                    }
                }
            }
        }
    }
}

/**
 * Merge expansion templates into existing PUNCHLINE_TEMPLATES.
 */
export function mergePunchlineTemplates(existing, expansion) {
    for (const [category, templates] of Object.entries(expansion)) {
        if (Array.isArray(existing[category])) {
            const existingSet = new Set(existing[category].map(t => t.toLowerCase()));
            for (const template of templates) {
                if (!existingSet.has(template.toLowerCase())) {
                    existing[category].push(template);
                }
            }
        } else {
            existing[category] = templates;
        }
    }
}
