/**
 * Merge expansion punchline phrases into existing PUNCHLINE_PHRASES.
 * Deduplicates by lowercase comparison.
 */
export function mergePunchlinePhrases2(existing, expansion) {
    for (const [family, moods] of Object.entries(expansion)) {
        if (!existing[family]) {
            existing[family] = moods;
        } else {
            for (const [mood, phrases] of Object.entries(moods)) {
                if (Array.isArray(existing[family][mood])) {
                    const set = new Set(existing[family][mood].map(p => p.toLowerCase()));
                    for (const phrase of phrases) {
                        if (!set.has(phrase.toLowerCase())) existing[family][mood].push(phrase);
                    }
                } else {
                    existing[family][mood] = phrases;
                }
            }
        }
    }
}

/**
 * Merge expansion templates into existing PUNCHLINE_TEMPLATES.
 * Deduplicates by lowercase comparison.
 */
export function mergePunchlineTemplates2(existing, expansion) {
    // Merge main expansion templates
    for (const [cat, templates] of Object.entries(expansion)) {
        if (Array.isArray(existing[cat])) {
            const set = new Set(existing[cat].map(t => t.toLowerCase()));
            for (const t of templates) {
                if (!set.has(t.toLowerCase())) existing[cat].push(t);
            }
        } else {
            existing[cat] = templates;
        }
    }
    // Merge extra templates (strip '_extra' suffix to merge into base categories)
    for (const [cat, templates] of Object.entries(PUNCHLINE_TEMPLATES_EXTRA2)) {
        const baseCat = cat.replace(/_extra$/, '');
        if (Array.isArray(existing[baseCat])) {
            const set = new Set(existing[baseCat].map(t => t.toLowerCase()));
            for (const t of templates) {
                if (!set.has(t.toLowerCase())) existing[baseCat].push(t);
            }
        } else {
            existing[baseCat] = templates;
        }
    }
}
